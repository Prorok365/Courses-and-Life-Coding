'''
 # @file three_num_sum.py
 # @brief Get three numbers from user and sum them together
 '''

first_number = input("Write first number: ")
second_number = input("Write second number: ")
third_number = input("Write third number: ")

main_sum = int(first_number) + int(second_number) + int(third_number)

print("If we sum this three numbers, we get", main_sum)

